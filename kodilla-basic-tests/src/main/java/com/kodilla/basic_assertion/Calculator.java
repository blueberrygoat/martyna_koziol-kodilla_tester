package com.kodilla.basic_assertion;

public class Calculator {
    public int sum(int a, int b) {
        return a + b;
    }

    public int sub(int a, int b) {
        return a - b;
    }
    public int squared(int a){
        return a * a;
    }
    public double squared2(double a) {
        return Math.pow(a, 2);
    }
}
